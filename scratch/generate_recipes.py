# Helper function to classify protein types
def get_protein_type(recipe):
    name = recipe["name"].lower()
    ing_names = [i["name"].lower() for i in recipe["ingredients"]]
    matchable = [m.lower() for m in recipe["matchableIngredients"]]
    
    # poultry
    if any(x in name or any(x in ing for ing in ing_names) or x in matchable for x in ["עוף", "פרגית", "פרגיות"]):
        return "עוף"
    # beef
    elif any(x in name or any(x in ing for ing in ing_names) or x in matchable for x in ["בקר", "קבב", "המבורגר בקר", "קציצות בקר", "סנייה"]):
        return "בקר"
    # fish
    elif any(x in name or any(x in ing for ing in ing_names) or x in matchable for x in ["סלמון", "דג", "אמנון", "טונה"]):
        return "דגים"
    # tofu
    elif any(x in name or any(x in ing for ing in ing_names) or x in matchable for x in ["טופו"]):
        return "טופו"
    # dairy / eggs
    elif any(x in name or any(x in ing for ing in ing_names) or x in matchable for x in ["ביצה", "ביצים", "גבינה", "גבינות", "קוטג'", "לאבנה", "יוגורט", "שמנת"]):
        return "גבינות וביצים"
    
    return "צמחוני"

